package br.com.ChronosAcademy.LogicaProgramacao;

public class Maior {
       /*
        Entre com dois valores e exiba o maior deles.
         */

    public static void main(String[] args) {

        int valorA = 5;
        int valorB = 4;

        if (valorA > valorB) {
            System.out.println("Meu maior valor é o: " + valorA);
        } else {
            System.out.println("Meu maior valor é o: " + valorB);
        }
    }
}
